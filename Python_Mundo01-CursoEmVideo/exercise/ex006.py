# Create a program that reads the number entered and displays its double, triple and square root:

numberInput = int(input('Enter a number: '))
print(f'The number entered was {numberInput}'
      f'\nits double is {numberInput * 2} '
      f'\nits triple is {numberInput * 3} '
      f'\nand its square root is {numberInput ** (1/2)}')
